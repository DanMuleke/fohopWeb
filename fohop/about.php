A: SECURING LIVELIHOODS
While signifcant Livelihoods programmes have been taking place over the years, very few people have benefted from such programmes. Tis
is because there are more people in rural areas than the services which were delivered in these areas. In other ways there is still more demand
of reaching other areas where the programme never took place but people are still suﬀering. Having this background the major objective of this
result area focus is to save and serve the rural societies from slavery situation arising due to lack of knowledge in how to get out of it and also to
complement the government eﬀort in securing rural livelihoods in the country.
Te above result area will be achieved through implementing the following strategies:1. Securing food security.
- Fountain of Hope recognises the important role played by small scale farmers in the communities. However, they are facing growing
challenges in the form of declining investment in agriculture, outdated skills and technology and lack of access to quality inputs. Tese small
scale farmers lack of innovation and development of agricultural enterprises. For example, small scale farmers are lacking access to knowledge in
irrigation farming, learning new sustainable agricultural production techniques and to diversify crop production to increase incomes. Terefore
Fountain of Hope is there to equip these small scale farmers with knowledge of how to go about the mentioned situations which enable farmers
to increase in agricultural production, thus Farmer training.
2. Enterprise development and access to markets
- Many people in rural areas have the capacity of doing diﬀerent activities but they do not have knowledge. Tis is where Fountain of Hope
(FOHOP) will intervene. People are to be trained in how to identify and run Income generating activities from the little resources they may acquire. People’s mind will be opened through sharing experiences and ideas on how others earn a living in other areas. Tis time now the benefciaries are to look around the raw materials in their areas which can be processed and bring income to their families.
- On access to markets, people are to be organised in groups so that it should be easier to sell and buy their produce, products, inputs and
materials. Government recognised buyers will be invited for a visit to area of operation and the also a committee will be formed to be researching for better markets.
- Cooperatives will be formed ensure total collective eﬀort and sustainability of the activities.
3. Improved Health activities
In ensuring that the programme should not be disturbed due bad health of the benefciaries, Fountain of Hope will conduct health and hygiene
like, sensitisation in some critical health issues like those of HIV and Aids, under fve children’s health, Malnutrition to children , actions on
planting of herbs for immunisation of the body, cooking demonstration of nutritious food from the local food, Securing freshwater use and
access to improved water source and improved sanitation facilities because healthy people can bring healthy results
4. Education Mobilisation
- Where there is literacy there is always smoother communication because a message can also be passed through writing so the needed
person just read it. Where there is illiteracy there is no such possibility. As such Fountain of Hope will introduce adult literacy education and
pre schools to its operation areas, will also mobilise parents and local leaders to make sure that children go to school. Not only that, but alsopromoting girls education in order to avoid repeating the same situation of women discrimination in education which has resulted into many
women becoming uneducated followed by de empowerment.
5. Orphanage care
Fountain of Hope shall secure the lives of vulnerable children by giving out support in education, feeding and parental care needed by children. In this case centre for orphans will be established. However, the support shall also go to other orphanage centres which need help.
6. Community Based Care centre
Te organisation will establish community Based care centres where children will be taken care of through education and social support, people
with HIV AIDS will be getting counselling and lessons, Maternity issues of mothers will be sensitised, elderly and disabled people will be provided with diﬀerent services.
7. Relief aid
Te organisation shall support the vulnerable community with diﬀerent relief items and services like food, clothes, diﬀerent facilities and diﬀerent charity services as per need.
8. Water supply for both irrigation and drinking.
Te organisation will mobilise the community and well wishers in securing water facilities for irrigation and drinking like Rope and washer
pumps, treadle pumps, bore holes, tap water and any other technology that contributes to water accessibility.
