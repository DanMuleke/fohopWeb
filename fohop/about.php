improvement in: Health, Agriculture production and food security, Education, Enterprise development and access to markets and Making the
community adapt to climate change impacts where practicing environmental friendly activities should be a routine in the lives of the people.However, the strategy does not imply that Fountain of Hope will do everything and in all rural areas, but it implies that Fountain Of Hope will
do what it can and will be able to form strategic alliances with other actors who are better placed to provide services that Fountain of Hope
cannot provide due to whatever reasons like fnances.
