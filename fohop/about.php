Te idea of securing Livelihoods and action on climate change must therefore always be taken into consideration in all matters of community
development.
Tere is no reason of sidelining, diﬀerentiating or gapping people’s lives in rural areas and those in urban areas where the latter have had the
majority with better living always. Ironically, majority of agricultural produce come from the rural areas where more farming takes place.
